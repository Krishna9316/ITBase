# formate method examples

# 1. Formatting user details for display
name = "Alice"
age = 28
formatted_string = "Name: {}, Age: {}".format(name, age)
print(formatted_string)

# 2. used in currency formatting
price = 49.99
formatted_price = "The price is: ${:.2f}".format(price)
print(formatted_price)

# 3. Data Validation and Reporting
score = 95
report = "Student scored: {} points".format(score)
print(report)
