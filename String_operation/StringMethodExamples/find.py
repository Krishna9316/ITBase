# find method examples

# 1. searching something in database
database = "Usernames: Krishna, Gaurav, Raj, Chetan"   
index_bob = database.find('Krishna')
print("Index of 'Krishna' in database:", index_bob)


# 2. serching something in inventory
inventory = "Items: laptop, mouse, keyboard, monitor"
index_keyboard = inventory.find('keyboard')
print("Index of 'keyboard' in inventory:", index_keyboard)

# 3. Contact searching in contact list
contacts = "Contacts: Krishna, Gaurav, Raj, Chetan"
index_jane = contacts.find('Chetan')
print("Index of 'Chetan' in contacts:", index_jane)

# 4. Name searching in list
names_list = "Names: Krishna, Gaurav, Raj, Chetan"
index_charlie = names_list.find('Raj')
print("Index of 'Raj' in names list:", index_charlie)


