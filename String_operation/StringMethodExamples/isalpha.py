# isalpha method example

# 1. User input validation for names
# name = input("Enter your name: ")
name = "Krishna"
if name.isalpha():
    print("Valid name")
else:
    print("Invalid name: Only alphabetic characters are allowed")
    
# 2. text processing
text = "HelloWorld"
if text.isalpha():
    print("The text contains only alphabetic characters")
else:
    print("The text contains non-alphabetic characters")

# 3. password policies

# password = input("Enter your password: ")
password = "SecurePass"
if password.isalpha():
    print("Valid password")
else:
    print("Invalid password: Only alphabetic characters are allowed")
    
# 4. City name validation
# city_name = input("Enter your city name: ")
city_name = "Springfield"
if city_name.isalpha():
    print("Valid city name")
else:
    print("Invalid city name: Only alphabetic characters are allowed")
    
