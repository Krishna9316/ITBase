# istitle method example

# 1. fullname validation
# fullname = input("Enter your full name: ")
fullname = "Krishna Verma"
if fullname.istitle():
    print("Valid full name")
else:
    print("Invalid full name")
    
# 2. Document title validation
# document_title = input("Enter the document title: ")
document_title = "The Great Gatsby"
if document_title.istitle():
    print("Valid document title")   
else:
    print("Invalid document title")
    
